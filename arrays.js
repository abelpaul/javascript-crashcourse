// arrays
let example1 = [3,4,5,6];

// length of array
console.log(example1.length)

// indexing array
console.log(example1[1])

// push method add value to end of index
example1.push(8);
console.log(example1);

// pop method remove value from end of the index
example1.pop();
console.log(example1);

// overwriting values 
example1[0] = 75;
console.log(example1);